package day5_unaryAssignmentRelationalOperators;

public class assignmentOperators {

	public static void main(String[] args) {

		int i=100;
		System.out.println(i);
		
		i+=1; //i=i+1=100+1=101
		System.out.println(i);
		
		i-=2; //i=i-2=101-2=99
		System.out.println(i);
		
		i*=3; //i=i*3=99*3=297
		System.out.println(i);
		
		i/=4; //i=i/4=297/4=74
		System.out.println(i);
		
		i%=5; 	//i=i%5=74%5=4
		System.out.println(i);
	}

}
