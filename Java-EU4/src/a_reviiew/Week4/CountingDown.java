package a_reviiew.Week4;

public class CountingDown {

	public static void main(String[] args) {

		
		int i=10;
		while(i>=0) {
			
			System.out.print(i+" ");
			
			i--;
		} System.out.println();
		
		int y=10;
		do {
			System.out.print(y+" ");
			y--;
		}while(y>=0);
		
		System.out.println();
		
		for(int l=10;l>=0;l--) {
			System.out.print(l+" ");
		}
	}

}
