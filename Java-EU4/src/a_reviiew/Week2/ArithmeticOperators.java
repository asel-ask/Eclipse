package a_reviiew.Week2;

public class ArithmeticOperators {

	public static void main(String[] args) {
		
		//Arithmetic Operator
		// +, -, *, /, %
		
		System.out.println(9 + 5);
		System.out.println(9 - 5);
		System.out.println(9 * 5);
		//division between whole numbers always result in whole number(int)
		System.out.println(9 / 5);
		//modulus or remainder operator will return the remaining part after division
		System.out.println(9 & 5);
		
		
		System.out.println(10%3);// it will be 1
		System.out.println(10/3); // it will be 3
		
	}

}
