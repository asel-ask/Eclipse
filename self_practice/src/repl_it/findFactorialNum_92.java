import java.util.Scanner;

public class findFactorialNum_92 {

	public static void main(String[] args) {
		 
		 
		    Scanner scan = new Scanner(System.in);
		    System.out.println("Enter integer");
		    int n = scan.nextInt();
		     
		    long sum=1;
		    for(long i=1; i<=n; i++){
		    	  sum*= i;
		     
		    } System.out.print(sum);
		       
		    }
		    
		    
		  }
		 

 

 
